#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	value;
	int	number;

	i = 0;
	value = 1;
	number = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			value = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = number * 10 + str[i] - '0';
		i++;
	}
	number = number * value;
	return (number);
}
